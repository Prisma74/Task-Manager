// Declaracion de var

var nombre = "Andres";
console.log(nombre); // Output: "Andres";

// Reasignacion de var

nombre = "Camilo";
console.log(nombre); // Output: "Camilo";

// Declaracion con let 

let edad = 25;
console.log(edad); // Output: 25;

// Reasignacion a let

edad = 30;
console.log(edad); // Output: 26;

// Declaracion con const

PI = 3.1416;
console.log(PI); // Output: 3.1416;

// Reasignacion de const

PI = 99999;
console.log(PI); // Error porque const no puede cambiar despues de asignarle un valor